    _git_commit = _detect_git_commit()
    _build_info = _load_build_info()
    logger.info(f"[DIAG] env={_detect_environment()} commit={_git_commit} db={os.environ.get('DB_NAME','polymarket_edge')}")

    state = StateManager()
    bus = EventBus()
    engine = TradingEngine(state, bus)

    # Phase 2 components
    engine.market_data = MarketDataFeed()
    engine.price_feeds = PriceFeedManager()
    engine.persistence = PersistenceService(db)

    # Reconstruct in-memory state from MongoDB (trades, positions)
    # This ensures after Railway restart the dashboard shows historical data
    await engine.persistence.load_state_from_db(state)
    _trades_loaded_from_db = len(state.trades)

    # Phase 3: register arb strategy (enabled by default)
    arb = ArbScanner()
    engine.register_strategy(arb)
    state.strategies["arb_scanner"].enabled = True
    arb_scanner_ref = arb

    # Phase 5: register crypto sniper strategy (enabled by default)
    sniper = CryptoSniper()
    sniper._tracker = strategy_tracker
    engine.register_strategy(sniper)
    state.strategies["crypto_sniper"].enabled = True
    crypto_sniper_ref = sniper

