def _detect_git_commit() -> str:
    """Best-effort git commit detection."""
    # 1. Env var (set by CI/CD)
    env = os.environ.get("GIT_COMMIT") or os.environ.get("RAILWAY_GIT_COMMIT_SHA") or os.environ.get("SOURCE_COMMIT")
    if env:
        return env[:12]
    # 2. Read .git directly
    for base in [Path(__file__).resolve().parent.parent, Path.cwd()]:
        head = base / ".git" / "HEAD"
        if head.is_file():
            ref = head.read_text().strip()
            if ref.startswith("ref:"):
                ref_path = base / ".git" / ref.split("ref: ", 1)[1]
                if ref_path.is_file():
                    return ref_path.read_text().strip()[:12]
            else:
                return ref[:12]
    # 3. git log
    try:
        r = subprocess.run(["git", "log", "--format=%H", "-1"], capture_output=True, text=True, timeout=3)
        if r.returncode == 0 and r.stdout.strip():
            return r.stdout.strip()[:12]
    except Exception:
        pass
    return "unknown"

def _load_build_info() -> dict:
    """Load build_info.json if present (generated by Dockerfile)."""
    for p in [Path(__file__).resolve().parent / "build_info.json", Path("/app/backend/build_info.json")]:
        if p.is_file():
            try:
                return _json.loads(p.read_text())
            except Exception:
                pass
    return {}

def _detect_environment() -> str:
    """Detect which environment we're in."""
    if os.environ.get("RAILWAY_ENVIRONMENT") or os.environ.get("RAILWAY_PROJECT_ID"):
        return "railway"
    if os.environ.get("EMERGENT_PREVIEW") or Path("/app/.emergent").exists():
        return "emergent_preview"
    return "local"


async def _ws_broadcast_loop():
    """Push state snapshots to all connected WebSocket clients every 2 seconds."""
    while True:
        try:
            if ws_clients and state:
                snapshot = state.snapshot()
                # Inject fill WS + execution health (same as /api/status)
                if clob_fill_ws_client:
                    snapshot["stats"]["health"]["fill_ws_connected"] = clob_fill_ws_client._connected
                    snapshot["stats"]["health"]["fill_ws_has_credentials"] = clob_fill_ws_client.has_credentials
                    snapshot["stats"]["health"]["fill_ws_health"] = clob_fill_ws_client.health
                if engine and engine.execution_engine:
                    exec_status = engine.execution_engine.live_adapter_status
                    snapshot["stats"]["health"]["fill_update_method"] = exec_status.get("fill_update_method", "polling")
                dead = set()
                for ws in ws_clients.copy():
                    try:
                        await ws.send_json(snapshot)
                    except Exception:
                        dead.add(ws)
                ws_clients.difference_update(dead)
            await asyncio.sleep(2)
        except asyncio.CancelledError:
            break
        except Exception:
            await asyncio.sleep(2)


async def _notify_trade_closed():
    """Push an immediate WS update when a trade is closed.

    This supplements the 2-second broadcast loop so the frontend
    sees the new data within milliseconds instead of waiting for
    the next poll cycle.
    """
    if not state:
        return
    try:
        snapshot = state.snapshot()
        dead = set()
        for ws in ws_clients.copy():
            try:
                await ws.send_json({**snapshot, "_event": "trade_closed"})
            except Exception:
                dead.add(ws)
        ws_clients.difference_update(dead)
    except Exception:
        pass


