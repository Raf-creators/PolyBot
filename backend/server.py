    if crypto_sniper_ref:
        for ex in crypto_sniper_ref._active_executions.values():
            for sig in crypto_sniper_ref._signals:
                if sig.id == ex.signal_id:
                    sniper_exec_map[sig.token_id] = {
                        "asset": sig.asset,
                        "direction": sig.direction,
                        "strike": sig.strike,
                        "spot_at_entry": sig.spot_price,
                        "fair_price": sig.fair_price,
                        "edge_at_entry": sig.edge_bps,
                        "confidence": sig.confidence,
                        "side": sig.side,
                    }
                    break
        for ex in crypto_sniper_ref._completed_executions[-50:]:
            if ex.status.value == "filled":
                for sig in crypto_sniper_ref._signals:
                    if sig.id == ex.signal_id and sig.token_id not in sniper_exec_map:
                        sniper_exec_map[sig.token_id] = {
                            "asset": sig.asset,
                            "direction": sig.direction,
                            "strike": sig.strike,
                            "spot_at_entry": sig.spot_price,
                            "fair_price": sig.fair_price,
                            "edge_at_entry": sig.edge_bps,
                            "confidence": sig.confidence,
                            "side": sig.side,
                        }
                        break

    by_strategy = {"weather": [], "crypto": [], "arb": [], "other": []}
    total_unrealized = 0.0

    # Build opened_at map: token_id -> earliest buy trade timestamp
    _opened_at_map = {}
    for t in state.trades:
        if t.side.value == "buy" and t.token_id not in _opened_at_map:
            _opened_at_map[t.token_id] = t.timestamp

    for pos in state.positions.values():
        bucket = classify_strategy(pos)
        d = pos.model_dump()

        # Market enrichment (same as /positions)
        market = state.get_market(pos.token_id)
        if market:
            d["end_date"] = market.end_date
            d["condition_id"] = market.condition_id
            d["current_price"] = market.mid_price or pos.current_price
            if market.mid_price and pos.avg_cost > 0:
                d["unrealized_pnl"] = round((market.mid_price - pos.avg_cost) * pos.size, 4)
            if market.end_date:
                try:
                    end_dt = datetime.fromisoformat(market.end_date.replace("Z", "+00:00"))
                    tte = (end_dt - now).total_seconds()
                    d["time_to_expiry_seconds"] = round(tte, 0)
                    d["hours_to_resolution"] = round(tte / 3600, 1) if tte > 0 else 0
                except (ValueError, TypeError):
                    d["time_to_expiry_seconds"] = None
                    d["hours_to_resolution"] = None
            else:
                d["time_to_expiry_seconds"] = None
                d["hours_to_resolution"] = None
        else:
            d["end_date"] = None
            d["time_to_expiry_seconds"] = None
            d["hours_to_resolution"] = None

        # Unrealized PnL percent
        cost_basis = pos.avg_cost * pos.size
        if cost_basis > 0:
            d["unrealized_pnl_pct"] = round(d.get("unrealized_pnl", pos.unrealized_pnl) / cost_basis * 100, 2)
        else:
            d["unrealized_pnl_pct"] = 0.0

        d["strategy_bucket"] = bucket
        total_unrealized += d.get("unrealized_pnl", pos.unrealized_pnl)

        # --- Resolution time enrichment ---
        opened_at_str = _opened_at_map.get(pos.token_id)
        d["opened_at"] = opened_at_str
        if opened_at_str:
            try:
                opened_dt = datetime.fromisoformat(opened_at_str.replace("Z", "+00:00"))
                d["time_open_seconds"] = round((now - opened_dt).total_seconds(), 0)
            except (ValueError, TypeError):
                d["time_open_seconds"] = None
        else:
            d["time_open_seconds"] = None

        # Strategy-specific enrichment
        if bucket == "weather":
            if pos.token_id in weather_exec_map:
                d["weather"] = weather_exec_map[pos.token_id]
            else:
                d["weather"] = _parse_weather_from_question(d.get("market_question", ""))

            # Derive resolution time from weather target_date or parsed question
            resolves_at = d.get("weather", {}).get("resolves_at_utc")
            target_date = d.get("weather", {}).get("target_date")
            if not resolves_at and target_date:
                resolves_at = f"{target_date}T23:59:00Z"

            d["resolves_at"] = resolves_at
            d["target_date"] = target_date
            if resolves_at:
                try:
                    res_dt = datetime.fromisoformat(resolves_at.replace("Z", "+00:00"))
                    ttr = (res_dt - now).total_seconds()
                    d["time_to_resolution_seconds"] = round(ttr, 0)
                    d["hours_to_resolution"] = round(ttr / 3600, 1) if ttr > 0 else 0
                    # Category
                    if ttr <= 0:
                        d["resolution_category"] = "resolved"
                    elif ttr <= 6 * 3600:
                        d["resolution_category"] = "near"
                    elif ttr <= 24 * 3600:
                        d["resolution_category"] = "medium"
                    else:
                        d["resolution_category"] = "long"
                except (ValueError, TypeError):
                    d["time_to_resolution_seconds"] = None
                    d["resolution_category"] = None
            else:
                d["time_to_resolution_seconds"] = d.get("time_to_expiry_seconds")
                d["resolution_category"] = None

            # --- Lifecycle enrichment ---
            if weather_trader_ref:
                lifecycle_evals = weather_trader_ref._lifecycle_evals
                lc = lifecycle_evals.get(pos.token_id)
                if lc:
                    d["lifecycle"] = {
                        "is_exit_candidate": lc.is_exit_candidate,
                        "exit_reason": lc.exit_reason,
                        "exit_reason_detail": lc.exit_reason_detail,
                        "profit_multiple": lc.profit_multiple,
                        "edge_at_entry": lc.edge_at_entry,
                        "current_edge_bps": lc.current_edge_bps,
                        "edge_decay_pct": lc.edge_decay_pct,
                        "current_model_prob": lc.current_model_prob,
                        "time_held_hours": lc.time_held_hours,
                        "lifecycle_mode": lc.lifecycle_mode,
                        "book_rank": lc.book_rank,
                        "book_score": lc.book_score,
                        "book_total": lc.book_total,
                    }
                else:
                    d["lifecycle"] = None

        elif bucket == "crypto":
            if pos.token_id in sniper_exec_map:
                d["sniper"] = sniper_exec_map[pos.token_id]
            else:
                d["sniper"] = _parse_sniper_from_question(d.get("market_question", ""))

        target = by_strategy.get(bucket, by_strategy["other"])
        target.append(d)

    # Strategy summaries
    summaries = {}
    attribution = strategy_tracker.get_strategy_attribution()
    for bucket_name in ["weather", "crypto", "arb"]:
        attr = attribution.get(bucket_name, {})
        positions = by_strategy.get(bucket_name, [])
        summaries[bucket_name] = {
            "open_positions": len(positions),
            "unrealized_pnl": round(sum(p.get("unrealized_pnl", 0) for p in positions), 4),
            "realized_pnl": attr.get("realized_pnl", 0),
            "total_pnl": round(
                attr.get("realized_pnl", 0) + sum(p.get("unrealized_pnl", 0) for p in positions), 4
            ),
            "trade_count": attr.get("trade_count", 0),
            "wins": attr.get("wins", 0),
            "losses": attr.get("losses", 0),
            "win_rate": attr.get("win_rate", 0),
            "capital_allocated": round(sum(p.get("size", 0) * p.get("avg_cost", 0) for p in positions), 2),
            "best_trade": attr.get("best_trade", 0),
            "worst_trade": attr.get("worst_trade", 0),
        }

    return {
        "positions": by_strategy,
        "summaries": summaries,
        "total_unrealized_pnl": round(total_unrealized, 4),
        "total_open": sum(len(v) for v in by_strategy.values()),
        "lifecycle": {
            "mode": weather_trader_ref.config.lifecycle_mode if weather_trader_ref else "off",
            "exit_candidates": sum(
                1 for p in by_strategy.get("weather", [])
                if p.get("lifecycle", {}) and p["lifecycle"].get("is_exit_candidate")
            ),
        },
    }



